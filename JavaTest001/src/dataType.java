
public class dataType {

	public static void main(String[] args) {
	
		String string = "Hossain";
		
         System.out.println(string.length());

         System.out.println(string.toLowerCase());
         System.out.println(string.toUpperCase());
         System.out.println(string.charAt(5));
         System.out.println(string);
        
    
        System.out.println(string.startsWith("S"));
     
        System.out.println(string.endsWith("n"));
        System.out.println(string.equalsIgnoreCase("Hossain"));
        System.out.println(string.equals("Hossain"));
        System.out.println(string.isEmpty());
        
	}
}